using System.Text.Json.Serialization;

namespace Build.Models;

/// <summary>
/// Represents the status of a harvest operation for a specific library and RID combination.
/// This is used in CI matrix jobs where each job handles one library+RID combination.
/// </summary>
public record RidHarvestStatus
{
    [JsonPropertyName("library_name")]
    public required string LibraryName { get; init; }

    [JsonPropertyName("rid")]
    public required string Rid { get; init; }

    [JsonPropertyName("triplet")]
    public required string Triplet { get; init; }

    [JsonPropertyName("success")]
    public required bool Success { get; init; }

    [JsonPropertyName("error_message")]
    public string? ErrorMessage { get; init; }

    [JsonPropertyName("timestamp")]
    public required DateTimeOffset Timestamp { get; init; }

    [JsonPropertyName("statistics")]
    public HarvestStatistics? Statistics { get; init; }
}

/// <summary>
/// Summary statistics from a harvest operation.
/// </summary>
public record HarvestStatistics
{
    [JsonPropertyName("primary_files_count")]
    public required int PrimaryFilesCount { get; init; }

    [JsonPropertyName("runtime_files_count")]
    public required int RuntimeFilesCount { get; init; }

    [JsonPropertyName("license_files_count")]
    public required int LicenseFilesCount { get; init; }

    [JsonPropertyName("deployed_packages_count")]
    public required int DeployedPackagesCount { get; init; }

    [JsonPropertyName("filtered_packages_count")]
    public required int FilteredPackagesCount { get; init; }

    [JsonPropertyName("deployment_strategy")]
    public required string DeploymentStrategy { get; init; }
}

/// <summary>
/// Consolidated harvest manifest for a library across all RIDs.
/// This is generated by the CI consolidation job from individual RID status files.
/// </summary>
public record HarvestManifest
{
    [JsonPropertyName("library_name")]
    public required string LibraryName { get; init; }

    [JsonPropertyName("generated_timestamp")]
    public required DateTimeOffset GeneratedTimestamp { get; init; }

    [JsonPropertyName("rids")]
    public required IReadOnlyList<RidHarvestStatus> Rids { get; init; }

    [JsonPropertyName("summary")]
    public required HarvestSummary Summary { get; init; }
}

/// <summary>
/// Summary information for the harvest manifest.
/// </summary>
public record HarvestSummary
{
    [JsonPropertyName("total_rids")]
    public required int TotalRids { get; init; }

    [JsonPropertyName("successful_rids")]
    public required int SuccessfulRids { get; init; }

    [JsonPropertyName("failed_rids")]
    public required int FailedRids { get; init; }

    [JsonPropertyName("success_rate")]
    public required double SuccessRate { get; init; }
}
